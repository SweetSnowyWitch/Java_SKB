package com.example.models;

public class Item {
    double price;
    double date;
    int id;

    public void setPrice(double price) {
        this.price = price;
    };

    public double getPrice() {
        return price;
    }

    public void setDate(double date) {
        this.date = date;
    };

    public double getDate() {
        return date;
    }

    public void setId(int id) {
        this.id = id;
    };

    public int getId() {
        return id;
    }

}
